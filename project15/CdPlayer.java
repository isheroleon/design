package com.zpkj.project15;
/**
 * cd播放器
 */
public class CdPlayer {
	

	public void on(){
		System.out.println("开启cd");
	}
	public void off(){
		System.out.println("关闭cd");
	}
	public void stop(){
		System.out.println("暂停");
	}
	public void play(){
		System.out.println("开始播放");
	}

}
